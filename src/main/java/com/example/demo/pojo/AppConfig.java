package com.example.demo.pojo;

public class AppConfig {
    private String id;
    private String ip;
    private int port;
    private String system_no;
    private String path;
    private String module_id;
    private String module_mc;
    private int field_count;
    private String field_out;
    private String field_in;

    public AppConfig() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }

    public String getSystem_no() {
        return system_no;
    }

    public void setSystem_no(String system_no) {
        this.system_no = system_no;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getModule_id() {
        return module_id;
    }

    public void setModule_id(String module_id) {
        this.module_id = module_id;
    }

    public String getModule_mc() {
        return module_mc;
    }

    public void setModule_mc(String module_mc) {
        this.module_mc = module_mc;
    }

    public int getField_count() {
        return field_count;
    }

    public void setField_count(int field_count) {
        this.field_count = field_count;
    }

    public String getField_out() {
        return field_out;
    }

    public void setField_out(String field_out) {
        this.field_out = field_out;
    }

    public String getField_in() {
        return field_in;
    }

    public void setField_in(String field_in) {
        this.field_in = field_in;
    }
}
